    private void LayDuLieu()
      {
        String SQL = "select * from HOADON";
        ResultSet rs = Main.connection.ExcuteQueryGetTable(SQL);
        Object [] obj = new Object[]{"STT","Mã HD","Tên Khách Hàng","Tên Nhân Viên", "Năm LẬP"};
        DefaultTableModel tableModel = new DefaultTableModel(obj,0);
        tblhd.setModel(tableModel);
        try
        {
            while(rs.next())
            {
                Object [] item = new Object[10];
                item[0] = tblhd.getRowCount() + 1;
                item[1] = rs.getString("mahd");
                item[2] =layTenKH(rs.getString("makh"));
                item[3]=LayTenNV(rs.getString("manv"));
                item[4] = rs.getString("ngaylaphd");
                tableModel.addRow(item);
            }
        }
        catch (Exception ex)
        {
            System.out.println(ex.toString());
        }
    }
    private void setSelectedCombobox(String tenLoai)
    {
        
        for(int i = 0; i < cbbmakh.getItemCount(); i++)
        {
            Object obj = cbbmakh.getItemAt(i);
            
            if(obj != null)
            {
                DisplayComboBoxModel m = (DisplayComboBoxModel) obj;
                if(tenLoai.equals(m.DisplayMember))
                {
                    cbbmakh.setSelectedItem(m);
                }
            }        
        }    
    }
