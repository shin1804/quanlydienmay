    private void LayKH(){
       String SQL = "select * from KHACHHANG";
        ResultSet rs = Main.connection.ExcuteQueryGetTable(SQL);
        DefaultComboBoxModel cbbModel = new DefaultComboBoxModel();
        try{
           while(rs.next()){
               DisplayComboBoxModel item = new DisplayComboBoxModel(rs.getString("tenkh"), rs.getString("makh"));
               cbbModel.addElement(item);
           }
           cbbmakh.setModel(cbbModel);
        }
        catch (Exception e) {
            System.out.println("ở đây");
        }
        
    }
